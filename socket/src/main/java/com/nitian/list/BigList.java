package com.nitian.list;

/**
 * 大数据list 建立虚拟list
 * 
 * @author 1036225283
 *
 */
public class BigList {

	/**
	 * 最大值
	 */
	private long max;

	/**
	 * 最小值
	 */
	private long min;

	/**
	 * length
	 */
	private long size;

	public long getMax() {
		return max;
	}

	public void setMax(long max) {
		this.max = max;
	}

	public long getMin() {
		return min;
	}

	public void setMin(long min) {
		this.min = min;
	}

	public long getSize() {
		return size;
	}

	public void setSize(long size) {
		this.size = size;
	}

}

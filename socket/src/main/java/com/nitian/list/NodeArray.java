package com.nitian.list;

public class NodeArray {

	/**
	 * 索引
	 */
	private long index;

	/**
	 * 值
	 */
	private long value;

	public long getIndex() {
		return index;
	}

	public void setIndex(long index) {
		this.index = index;
	}

	public long getValue() {
		return value;
	}

	public void setValue(long value) {
		this.value = value;
	}

}
